function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)]
}

export function stoicProvoke(mode) {
  const pool = mode === 'business' ? BUSINESS_PROVOCATIONS : PHILOSOPHY_PROVOCATIONS
  const item = pick(pool)
  return { type: 'question', text: item.text, label: item.label }
}
